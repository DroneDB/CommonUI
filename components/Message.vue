<template>
    <div v-if="$parent[bindTo]" class="ui message" :class="className">
        <i class="close icon" @click="dismiss"></i>
        <span v-html="$parent[bindTo]" />
    </div>
</template>

<script>
export default {
  props: {
      bindTo: {
          type: String,
          default: "message"
      },
      className: {
          type: String,
          default: "warning"
      }
  },
  methods: {
      dismiss: function(){
          this.$parent[this.bindTo] = "";
      }
  }
}
</script>

